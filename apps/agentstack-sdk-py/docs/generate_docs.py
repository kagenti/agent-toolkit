def main() -> None:
    parser = argparse.ArgumentParser(description="Generate Python SDK reference docs (MDX) from exports_structure.json.")
    parser.add_argument(
        "--json-file",
        type=Path,
        help="Path to the exports_structure.json manifest",
    )
    parser.add_argument(
        "--docs-dir",
        type=Path,
        help=f"Root docs output directory",
    )
    parser.add_argument(
        "--templates-dir",
        type=Path,
        help=f"Jinja2 templates directory",
    )
    args = parser.parse_args()

    json_file: Path = args.json_file
    docs_dir: Path = args.docs_dir
    templates_dir: Path = args.templates_dir

    if not json_file.exists():
        raise FileNotFoundError(
            f"exports_structure.json not found at {json_file}.\n"
            "Run `mise x -- uv run introspect_exports.py` first."
        )

    data = json.loads(json_file.read_text())
    env = _make_env(templates_dir)
    _ensure_module_maps()

    version = "development"
    out_dir = docs_dir / version / "reference" / "python-sdk"
    out_dir.mkdir(parents=True, exist_ok=True)

    # ── overview + module pages (as before) ──────────────────────────────────
    content = render_overview(env, version)
    (out_dir / "overview.mdx").write_text(content)

    for cfg in PAGE_CONFIG:
        content = render_page(env, cfg, data, version)
        (out_dir / f"{cfg['filename']}.mdx").write_text(content)

    # ── auto-generated symbol pages ───────────────────────────────────────────
    # Collect all unique symbols across every module key in the JSON.
    # Dedup by (name, origin) so each real symbol gets exactly one page,
    # placed under the most specific matching PAGE_CONFIG module directory.
    generated_symbols: set[tuple[str, str]] = set()
    symbol_count = 0

    for json_key, items in data.items():
        for item in items:
            kind = item.get("kind")
            if kind in _SKIP_KINDS:
                continue
            name: str = item.get("name", "")
            origin: str = item.get("origin", json_key)
            if not name:
                continue

            dedup_key = (name, origin)
            if dedup_key in generated_symbols:
                continue
            generated_symbols.add(dedup_key)

            import_path = _symbol_import_path(item)
            parent_filename = _symbol_page_parent_filename(item)
            base_href = f"/{version}/reference/python-sdk"
            breadcrumb_ancestors = _symbol_breadcrumb_ancestors(parent_filename)

            symbol_dir = out_dir / parent_filename
            symbol_dir.mkdir(parents=True, exist_ok=True)

            if kind == "class":
                content = render_class_page(env, item, import_path, breadcrumb_ancestors, base_href)
            elif kind == "function":
                content = render_function_page(env, item, import_path, breadcrumb_ancestors, base_href)
            else:
                continue

            (symbol_dir / f"{name}.mdx").write_text(content)
            symbol_count += 1

    module_count = 1 + len(PAGE_CONFIG)
    print(
        f"\nGenerated {module_count} module pages + {symbol_count} symbol pages"
        f" in {version}/reference/python-sdk/"
    )


if __name__ == "__main__":
    main()
